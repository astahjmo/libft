#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, t_size n)
{
	while ((n-- > 0) && (*s1 || *s2))
	{
		if (*s1 != *s2)
			break;
		s1++;
		s2++;
	}
	return (s1 - s2);
}
